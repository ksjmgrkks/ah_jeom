package com.ssac.ah_jeom.src.main.home.fragments

import android.os.Bundle
import android.view.View
import com.bumptech.glide.Glide
import com.ssac.ah_jeom.R
import com.ssac.ah_jeom.config.BaseFragment
import com.ssac.ah_jeom.databinding.FragmentModernArtBinding

class ModernArtFragment(private val imageUrl: String) :
    BaseFragment<FragmentModernArtBinding>(FragmentModernArtBinding::bind, R.layout.fragment_modern_art) {
    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)

        Glide.with(this).load(imageUrl).into(binding.fragmentModernArtImage)

    }
}